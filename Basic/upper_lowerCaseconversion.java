import java.util.*;
 public class upper_lowerCaseconversion {
     public static void main(String[] args){
         Scanner sc=new Scanner(System.in);
         System.out.print("enter some words :-");
           String a=sc.nextLine();
         System.out.println("this word uppercase conversion is "+ a.toUpperCase());
         System.out.println("this word lowercase conversion is "+ a.toLowerCase());
     }
}
